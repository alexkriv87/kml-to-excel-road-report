def get_base_path():
    """Возвращает путь к папке, где находится исполняемый файл"""
    if getattr(sys, 'frozen', False):
        # Запущено из exe
        return Path(sys.executable).parent
    else:
        # Запущено из скрипта
        return Path(__file__).parent

